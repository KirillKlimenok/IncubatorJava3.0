package dercochenko.com.Modules.Task10;

public class Calculator {
    public static int getSum(int i, int j) {
        return i + j;
    }

    public static float getSum(float i, float j) {
        return i + j;
    }

    public static double getSum(double i, double j) {
        return i + j;
    }

    public static int getDifference(int i, int j) {
        return i - j;
    }

    public static float getDifference(float i, float j) {
        return i - j;
    }

    public static double getDifference(double i, double j) {
        return i - j;
    }

    public static int getComposition(int i, int j) {
        return i * j;
    }

    public static double getComposition(double i, double j) {
        return i * j;
    }

    public static float getComposition(float i, float j) {
        return i * j;
    }

    public static double getDivision(double i, double j) {
        return i / j;
    }

    public static double getDivision(float i, float j) {
        return i / j;
    }

    public static double sqrt(double result) {
        return sqrt(result);
    }

    public static int sqrt(int result) {
        return sqrt(result);
    }

    public static float sqrt(float result) {
        return sqrt(result);
    }

}
